for i in range(10): #반복 조건
    print(i,end=' ') #반복 조건 참인 동안 반복

print() #한 줄 띄우기

for i in range(1,10): #반복 조건
    print(i,end=' ')

print()

for i in range(1,11,2): #반복 조건
    print(i,end=' ') #홀수 출력

print()
for i in range(10,0,-1):
    print(i,end=' ')