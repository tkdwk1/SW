num1=47 #num1변수에 정수 47 저장
num2=18 #num2변수에 정수 18 저장

res1=num1+num2 #res1 변수에 num1+num2 결과 저장
res2=num1-num2 #res2 변수에 num1-num2 결과 저장
res3=num1*num2 #res3 변수에 num1*num2 결과 저장
res4=num1//num2 #res4 변수에 num1//num2 결과 저장
print('47과 18의 합은 :',res1)
print('47과 18의 차는 :',res2)
print('47과 18의 곱은 :',res3)
print('47과 18의 나눗셈은 :',res4)

print(num1,'과',num2,'합은 :',num1+num2)
print(num1,'과',num2,'차는 :',num1-num2)
print(num1,'과',num2,'곱은 :',num1*num2)
print(num1,'과',num2,'나눗셈은 :',num1//num2)
